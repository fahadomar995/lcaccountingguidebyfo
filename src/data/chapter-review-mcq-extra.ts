import type { ReviewItem } from './chapter-review-bank';

type Tuple = [
  string,                 // id suffix (unique within chapter, prefix added)
  string,                 // prompt
  string[],               // options (correct first)
  string,                 // explanation
  string?,                // sectionLink
];

function mk(chapterId: number, rows: Tuple[]): ReviewItem[] {
  return rows.map(([suffix, prompt, opts, explanation, sectionLink]) => {
    // Shuffle options deterministically per id so the correct answer is not
    // always option A, but stays stable across renders.
    const seed = suffix.split('').reduce((a, c) => a + c.charCodeAt(0), 0);
    const order = opts.map((_, i) => i).sort((a, b) => {
      const ra = Math.sin(seed + a * 9301) * 10000;
      const rb = Math.sin(seed + b * 9301) * 10000;
      return (ra - Math.floor(ra)) - (rb - Math.floor(rb));
    });
    const shuffled = order.map(i => opts[i]);
    const correctIndex = order.indexOf(0);
    return {
      id: `rx${chapterId}-${suffix}`,
      chapterId,
      type: 'mcq' as const,
      prompt,
      itemData: {
        type: 'mcq' as const,
        data: { options: shuffled, correctIndex },
      },
      explanation,
      sectionLink,
    };
  });
}

